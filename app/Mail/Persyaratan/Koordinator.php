<?php

namespace App\Mail\Persyaratan;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Mail\Mailable;
use Illuminate\Queue\SerializesModels;

class Koordinator extends Mailable
{
    use Queueable, SerializesModels;

    /**
     * Create a new message instance.
     *
     * @return void
     */
    public function __construct($data)
    {
        $this->data = $data;
    }

    /**
     * Build the message.
     *
     * @return $this
     */
    public function build()
    {
        return $this->from('ulo@kominfo.co.id')->subject('Pemenuhan Persyaratan')->view('layouts.frontend.email_persyaratan.koordinator')->with('data', $this->data);
    }
}
